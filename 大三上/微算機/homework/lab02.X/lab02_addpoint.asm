    #include<p18f4520.inc>
	CONFIG OSC = INTIO67
	CONFIG WDT = OFF
	org 0x00 ; pc = 0x00
MOVLB 0X1
MOVLW 0XA1
MOVWF 0X00,1
MOVLW 0XB2
MOVWF 0X01,1
MOVLW 0XC3
MOVWF 0X02,1
MOVLW 0XD4
MOVWF 0X03,1
MOVLW 0XE5
MOVWF 0X04,1
MOVLW 0X05
MOVWF 0X05,1

MOVLW 0XA7
MOVWF 0X10,1
MOVLW 0XB3
MOVWF 0X11,1
MOVLW 0XC9
MOVWF 0X12,1
MOVLW 0XF6
MOVWF 0X13,1
MOVLW 0X04
MOVWF 0X14,1
	
MOVLW 0X09
MOVWF 0X29,1	
	
LFSR 0,0X100
LFSR 1,0X110
LFSR 2,0X120
	
merge:
    CLRF WREG
    CPFSGT 0X29,1
	GOTO exit
    CPFSGT 0X14,1
	GOTO SFR0_to_arr
    CPFSGT 0X05,1
	GOTO SFR1_to_arr
    ADDWF INDF0,0 ;WREG = [INDF0]
    CPFSGT INDF1
	GOTO SFR1_to_arr
    GOTO SFR0_to_arr
SFR0_to_arr:
    DECF 0X05,1,1
    DECF 0X29,1,1
    CLRF WREG
    ADDWF POSTINC0,0 ;WREG = FSR0 ;FSR0++
    MOVWF POSTINC2   ;FSR2 = WREG ;FSR2++
    GOTO merge
SFR1_to_arr:
    DECF 0X14,1,1
    DECF 0X29,1,1
    CLRF WREG
    ADDWF POSTINC1,0
    MOVWF POSTINC2
    GOTO merge
exit:
    NOP
end